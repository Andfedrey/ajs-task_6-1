import getDestructor from '../src/destruction';
import getHeroSort from '../src/heroSort';

test('get destruction', () => {
  const character = {
    name: 'Лучник',
    type: 'Bowman',
    health: 50,
    level: 3,
    attack: 40,
    defence: 10,
    special: [
      {
        id: 8,
        name: 'Двойной выстрел',
        icon: 'http://...',
        description: 'Двойной выстрел наносит двойной урон',
      },
      {
        id: 9,
        name: 'Нокаутирующий удар',
        icon: 'http://...',
      },
    ],
  };
  expect(getDestructor(character)).toEqual([
    {
      id: 8,
      name: 'Двойной выстрел',
      icon: 'http://...',
      description: 'Двойной выстрел наносит двойной урон',
    }]);
});

test('sorting heroe`s stats', () => {
  const objHero = {
    name: 'мечник', health: 10, level: 2, attack: 80, defence: 40,
  };
  const seachStats = ['name', 'level'];
  expect(getHeroSort(objHero, seachStats)).toEqual([
    { key: 'name', value: 'мечник' },
    { key: 'level', value: 2 },
    { key: 'attack', value: 80 },
    { key: 'defence', value: 40 },
    { key: 'health', value: 10 },
  ]);
});
